# Задача 18: Требуется найти в массиве A[1..N] самый близкий по величине элемент к заданному числу X. 
# Пользователь в первой строке вводит натуральное число N – количество элементов в массиве. 
# В последующих  строках записаны N целых чисел Ai. Последняя строка содержит число X

# *Пример:*

# 5
#     1 2 3 4 5
#     6
#     -> 5


import random

# Ввод количества элементов и числа X
n = int(input("Введите количество элементов содержит Ваш массив: "))
x = int(input("Введите число X: "))

# Генерация массива случайных чисел
a = [random.randint(-100, 100) for _ in range(n)]
print("Массив, состоящий из случайных чисел: ", a)

# Инициализация минимальной разницы и ближайшего элемента
min_diff = abs(x - a[0])
closest = a[0]

# Поиск ближайшего элемента
for i in range(1, n):
    diff = abs(x - a[i])
    if diff < min_diff:
        min_diff = diff
        closest = a[i]

# Вывод результата
print(f"Самый близкий элемент к {x} в Вашем массиве {a} равен {closest}")
